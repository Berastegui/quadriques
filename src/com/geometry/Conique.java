package com.geometry;

public class Conique {
	private double a,b,c,d,e,f;

	public Conique(double a, double b, double c, double d, double e, double f) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
		this.e = e;
		this.f = f;
	}

	public double getA() {
		return a;
	}

	public void setA(double a) {
		this.a = a;
	}

	public double getB() {
		return b;
	}

	public void setB(double b) {
		this.b = b;
	}

	public double getC() {
		return c;
	}

	public void setC(double c) {
		this.c = c;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}

	public double getE() {
		return e;
	}

	public void setE(double e) {
		this.e = e;
	}

	public double getF() {
		return f;
	}

	public void setF(double f) {
		this.f = f;
	}
	
	public Trinome createTrinome(double y) {
		return new Trinome(a, d + c*y, f + e*y + b*y*y);
	}
	
	public void print() {
		System.out.println(a + ", "+b + ", "+ c + ", "+ d + ", "+ e + ", "+ f);
	}
}
